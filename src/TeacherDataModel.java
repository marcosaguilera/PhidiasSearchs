public class TeacherDataModel {
    private String codigo_std;
    private String nombre;
    private String apellido;
    private String email;

    /**
     * @return the codigo_std
     */
    public String getCodigo_std() {
        return codigo_std;
    }

    /**
     * @param codigo_std the codigo_std to set
     */
    public void setCodigo_std(String codigo_std) {
        this.codigo_std = codigo_std;
    }

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the apellido
     */
    public String getApellido() {
        return apellido;
    }

    /**
     * @param apellido the apellido to set
     */
    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }
    
    
    
}
